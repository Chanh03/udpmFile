    void updateCanhBao() {
        int i = 2000;
        timer = new Timer(i, new ActionListener() {
            int progress = 0;

            @Override
            public void actionPerformed(ActionEvent e) {
                // Dừng timer nếu đã hoàn thành
                lblCanhBao.setText("");
                timer.stop();
            }
        });

        //chạy timer ở trên
        timer.start();
    }

    void showHideButton() {
        if (ckbShowHide.isSelected()) {
            txtMatKhau.setEchoChar((char) 0);
            ckbShowHide.setText("Hide");
        } else {
            txtMatKhau.setEchoChar('*');
            ckbShowHide.setText("Show");
        }
    }

